package Week3;

/**
 * FiveToTwenty
 */
public class FiveToTwenty {
    public static void main(String [] args)
    {
        // Use a loop to print out the numbers
        int i = 5;
        while (i <= 20) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println(); // Finish with a new line.
    }
}