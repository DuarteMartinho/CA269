package Week3;

/**
 * FiveToTwenty
 */
public class FiveToTwentyFor {
    public static void main(String [] args)
    {
        // Use a loop to print out the numbers
        for (int i = 5; i <= 20; i++) {
            System.out.print(i + " ");

        }
        System.out.println(); // Finish with a new line.
    }
}