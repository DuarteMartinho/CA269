package Week3;

/**
 * TenToOne
 */
public class TenToOneFor {

    public static void main(String [] args)
    {
        // Use a loop to print out the numbers
        for (int i = 10; i > 0; i--) {
            System.out.print(i + " ");
            
        }
        System.out.println(); // Finish with a new line.
    }
}